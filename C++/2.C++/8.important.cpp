#include <iostream>
using namespace std;
int main()
{
    // ****************************reference variable************************************
    float d = 3.3f;
    long double e = 3.4L;
    cout << " the size of 3.4 is :  " << sizeof(3.4) << endl;
    cout << " the size of 3.4f is :  " << sizeof(3.4f) << endl;
    cout << " the size of 3.4F is :  " << sizeof(3.4F) << endl;
    cout << " the size of 3.4l is :  " << sizeof(3.4l) << endl;
    cout << " the size of 3.4L is :  " << sizeof(3.4L) << endl;
}
